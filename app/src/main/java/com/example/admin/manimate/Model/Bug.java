public class Bug {

        String bugProid;

        String bugtyp;
        String bugname;
        String bugdis;

        String bugasn;

    public String getBugProid() {
        return bugProid;
    }

    public void setBugProid(String bugProid) {
        this.bugProid = bugProid;
    }

    public String getBugtyp() {
        return bugtyp;
    }

    public void setBugtyp(String bugtyp) {
        this.bugtyp = bugtyp;
    }

    public String getBugname() {
        return bugname;
    }

    public void setBugname(String bugname) {
        this.bugname = bugname;
    }

    public String getBugdis() {
        return bugdis;
    }

    public void setBugdis(String bugdis) {
        this.bugdis = bugdis;
    }

    public String getBugasn() {
        return bugasn;
    }

    public void setBugasn(String bugasn) {
        this.bugasn = bugasn;
    }
}
